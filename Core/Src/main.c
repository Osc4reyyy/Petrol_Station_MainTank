/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
	/* USER CODE BEGIN 5 */
	  uint8_t received_pump_id;

	  /* Infinite loop */
	  for(;;)
	  {
	    // 1. Wait for a pump signal from the EXTI interrupt queue
	    if (osMessageQueueGet(PumpQueueHandle, &received_pump_id, NULL, osWaitForever) == osOK)
	    {
	      // 2. We received a pump signal! Take the Mutex.
	      if (osMutexAcquire(TankMutexHandle, osWaitForever) == osOK)
	      {
	        // 3. Critial Section: Update the single variable
	        if (petrol_tank > 0)
	        {
	          petrol_tank--;
	        }

	        // 4. Immediately release the Mutex so other pumps can access it
	        osMutexRelease(TankMutexHandle);
	      }
	    }
	  }
	/* USER CODE END 5 */
}

